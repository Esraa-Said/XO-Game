void startGame(void)
{
	int i,j,dim = 10,outerY=2,outerX=1;
	Nokia5110_ClearBuffer(); 
	for(i=0;i<3;i++)
	{
		for(j=0;j<3;j++)
		{
			Nokia5110_PrintBMP(j*SQUAREHEIGHT + outerX, (SQUAREHEIGHT - 1) + (dim*i) + outerY, square, 0);
			Nokia5110_DisplayBuffer();
			outerX += 1;
		}
		outerX = 1;
		outerY += 5;
	}	
}

